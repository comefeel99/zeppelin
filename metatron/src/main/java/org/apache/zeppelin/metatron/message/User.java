package org.apache.zeppelin.metatron.message;

public class User {
  String type;
  String username;
  String fullName;
  String email;

  public String getType() {
    return type;
  }

  public String getUsername() {
    return username;
  }

  public String getFullName() {
    return fullName;
  }

  public String getEmail() {
    return email;
  }
}
